var (
	datacenterOnce    sync.Once
	datacenterNets    []*net.IPNet
)

func initDatacenterNets() {
	datacenterOnce.Do(func() {
		for _, cidr := range datacenterCIDRStrings {
			_, ipnet, err := net.ParseCIDR(cidr)
			if err == nil {
				datacenterNets = append(datacenterNets, ipnet)
			}
		}
	})
}

// IsDatacenterIP returns true if ip belongs to a known cloud / hosting
// provider range. Thread-safe after the first call triggers initialisation.
func IsDatacenterIP(ipStr string) bool {
	initDatacenterNets()
	ip := net.ParseIP(ipStr)
	if ip == nil {
		return false
	}
	for _, n := range datacenterNets {
		if n.Contains(ip) {
			return true
		}
	}
	return false
}
